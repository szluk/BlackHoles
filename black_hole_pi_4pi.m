cosb = (cos(B)+cos(A)*cos(C))/(sin(A)*sin(C))
cosc = (cos(C)+cos(A)*cos(B))/(sin(A)*sin(B))
cosd = (cos(D)+cos(E)*cos(E))/(sin(E)*sin(E))
cose = (cos(E)+cos(D)*cos(E))/(sin(D)*sin(E))

a = acos(cosa)
adeg = a*180/pi

b = acos(cosb)
bdeg = b*180/pi

c = acos(cosc)
cdeg = c*180/pi

d = acos(cosd)
ddeg = d*180/pi

e = acos(cose)
edeg = e*180/pi

